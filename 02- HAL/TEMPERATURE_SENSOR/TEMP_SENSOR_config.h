#ifndef _HAL_TEMP_SENSOR_CONFIG_H_
#define _HAL_TEMP_SENSOR_CONFIG_H_

/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
  _____
| Temp.  |--------------------Temperature Sensor OutPut Voltage -------------->	ADC InPut Voltage (PORTA Channels)
| Sensor |-------------------> Power Voltage
| _____ |-------------------> Ground

-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	*/
/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * Select the Temperature sensor port -------------------->>		TEMP_SENSOR_PORT
**ADC PORT 	---------------------------------------------->>    	DIO_U8PORT_A
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	*/
#define TEMP_SENSOR_PORT										  	DIO_U8PORT_A

/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * Select the Temperature sensor Pin,
 *  which will be an input for the ADC channel ----------->>		TEMP_SENSOR_PIN
**PA0 (ADC0) ---------------------------------------------->>   	 DIO_U8PIN0
**PA1 (ADC1) ---------------------------------------------->>   	 DIO_U8PIN1
**PA2 (ADC2) ---------------------------------------------->>   	 DIO_U8PIN2
**PA3 (ADC3) ---------------------------------------------->>   	 DIO_U8PIN3
**PA4 (ADC4) ---------------------------------------------->>    	 DIO_U8PIN4
**PA5 (ADC5) ---------------------------------------------->>    	 DIO_U8PIN5
**PA6 (ADC6) ---------------------------------------------->>   	 DIO_U8PIN6
**PA7 (ADC7) ---------------------------------------------->>   	 DIO_U8PIN7
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	*/
#define TEMP_SENSOR_PIN										      	 DIO_U8PIN0


/*---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * Select the Temperature sensor direction ---------------->> 		TEMP_SENSOR_DIRECTION
**INPUT Device -------------------------------------------->>    	DIO_U8INPUT
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	*/
#define TEMP_SENSOR_DIRECTION										 DIO_U8INPUT


/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 * Select the Temperature sensor state  ------------------->>		TEMP_SENSOR_STATE
**FLOAT INPUT 	---------------------------------------->>    	DIO_U8FLOAT
**PULL UP INPUT ---------------------------------------->>    		DIO_U8PULL_UP
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	*/
#define TEMP_SENSOR_STATE												DIO_U8FLOAT


/*-------------------------------------------Has been defined in ADC Driver-------------------------------------------------------------------------------------------------
* Select the ADC trigger source ------------------------->>			TEMSENSOR_ADC_TRIGGER_SOURCE
* Free Running Mode	------------------------------------>>			ADC_U8FreeRunningMode
*External Interrupt request 0  ---------------------------->>   	ADC_U8ExternalInterruptRequest_0
*Timer Interrupt request 0 ------------------------------->>   		ADC_U8TimerInterruptRequest_0
*Timer Interrupt request 1  ------------------------------>>   		ADC_U8TimerInterruptRequest_1

#define ADC_AUTO_TRIGGERING_SOURCE					ADC_U8ExternalInterruptRequest_0

-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	*/

#endif /* _HAL_TEMP_SENSOR_CONFIG_H_ */
